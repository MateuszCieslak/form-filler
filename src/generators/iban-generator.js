const getRandomBank = () => bankCodes[getRandomInt(0, bankCodes.length - 1)];
const getRandomPart = () => prependZeros(getRandomInt(0, 9999999999999999), 16);

export function getRandomIban() {
    const number = getRandomBank() + getRandomPart();
    const controlSum = getIbanControlSumField(number);
    return 'PL ' + format(controlSum + number);
}

function getIbanControlSumField(base) {
    const rest = modulo(base + "252100", 97);
    const controlNumber = 98 - rest;
    return prependZeros(controlNumber, 2);
}

function modulo(divident, divisor) {
    const partLength = 10;

    while (divident.length > partLength) {
        const part = divident.substring(0, partLength);
        divident = (part % divisor) + divident.substring(partLength);
    }

    return divident % divisor;
}

function format(iban) {
    let formated = iban.substring(0, 2);
    for (let i = 2; i < 26; i += 4) {
        formated = formated + ' ' + iban.substring(i, i + 4);
    }
    return formated;
}


